//program to find the grade for the student using switch statement.
var grade='a';

switch (grade) {
    case 'a':
        console.log("grade a, excellent");
        break;

    case 'b':
        console.log("grade b, good");
        break;

    case 'c':
        console.log("grade c, not good");
        break;

    default:
        console.log("need to improve");
        break;
}